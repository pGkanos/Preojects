SFmin=10^6;
for k=-0.2:0.1:100
    for s=150:0.1:1000

        F(1)=k/s*(exp((-1/2*(2.5/s)^2))-exp((-1/2*(2.7/s)^2)))-0.0163;
        F(2)=k/s*(exp((-1/2*(2.5/s)^2))-exp((-1/2*(2.7/s)^2))+exp((-1/2*(1.4/s)^2))-exp((-1/2*(1.2/s)^2)))-0.0238;
        F(3)=k/s*(exp((-1/2*(2.5/s)^2))-exp((-1/2*(2.7/s)^2))+exp((-1/2*(1.4/s)^2))-exp((-1/2*(1.2/s)^2))+exp((-1/2*(2/s)^2))-exp((-1/2*(1.8/s)^2)))-0.0385;
        F(4)=k/s*(exp((-1/2*(2.5/s)^2))-exp((-1/2*(2.7/s)^2))+exp((-1/2*(1.4/s)^2))-exp((-1/2*(1.2/s)^2))+exp((-1/2*(2/s)^2))-exp((-1/2*(1.8/s)^2))+exp((-1/2*(3.9/s)^2))-exp((-1/2*(3.7/s)^2)))-0.047;
        
        SF=F(1)^2 + F(2)^2 + F(3)^2 + F(4)^2;
        if SF<SFmin 
           SFmin=SF;
           mink=k;
           mins=s;
        end
    end
end

x=[-400:400];
delta=mink/mins*(exp((-1/2)*(x./mins).^2));
plot(delta);
