SFmin=10^6;
for k=1:0.1:100
    for s=1:0.1:1000

        F(1)=k/s*(exp((-1/2*(31.5/s)^2))-exp((-1/2*(27.9/s)^2)))-0.021;
        F(2)=k/s*(exp((-1/2*(31.5/s)^2))-exp((-1/2*(27.9/s)^2))+exp((-1/2*(15.4/s)^2))-exp((-1/2*(12.5/s)^2)))-0.0328;
        F(3)=k/s*(exp((-1/2*(31.5/s)^2))-exp((-1/2*(27.9/s)^2))+exp((-1/2*(15.4/s)^2))-exp((-1/2*(12.5/s)^2))+exp((-1/2*(19/s)^2))-exp((-1/2*(18.3/s)^2)))-0.0412;
        F(4)=k/s*(exp((-1/2*(31.5/s)^2))-exp((-1/2*(27.9/s)^2))+exp((-1/2*(15.4/s)^2))-exp((-1/2*(12.5/s)^2))+exp((-1/2*(19/s)^2))-exp((-1/2*(18.3/s)^2))+exp((-1/2*(34.4/s)^2))-exp((-1/2*(36.6/s)^2)))-0.05;
        
        SF=F(1)^2 + F(2)^2 + F(3)^2 + F(4)^2;
        if SF<SFmin 
           SFmin=SF;
           mink=k;
           mins=s;
        end
    end
end

x=[-400:400];
delta=mink/mins*(exp((-1/2)*(x./mins).^2));
plot(delta);